# str = string = nome, texto, conjunto de caracteres.
# int = integer = número inteiro.
# float = número decimal.
# bool = boolean = True ou False.
# list = lista = [1, 2, 3, 4, 5]
# tuple = tupla = (1, 2, 3, 4, 5)
# dict = dicionário = {'nome': 'Gustavo', 'sobrenome': 'Guilhon'}
# set = conjunto = {1, 2, 3, 4, 5}

nome = 'Gustavo' # criando uma variável do tipo string guardando valor Gustavo
idade = 22 # criando uma variável do tipo inteiro guardando valor 22
altura = 1.74 # criando uma variável do tipo float guardando valor 1.74
notas = [10, 9, 8, 7, 6] # criando uma variável do tipo lista guardando valores 10, 9, 8, 7, 6
status = True # criando uma variável do tipo boolean guardando valor True

#TESTES

#Imprimir variáveis
print(nome, 'tem', idade, 'anos e', altura, 'm de altura') # imprimindo na tela o valor das variáveis nome, idade e altura
print('As notas do aluno são:', notas) # imprimindo na tela o valor da variável notas
print('O status do aluno é:', status) # imprimindo na tela o valor da variável

#Checar tipos de variáveis
print(type(nome)) # imprimindo na tela o tipo da variável nome
print(type(idade)) # imprimindo na tela o tipo da variável idade
print(type(altura)) # imprimindo na tela o tipo da variável altura
print(type(notas)) # imprimindo na tela o tipo da variável notas
print((type(status))) # imprimindo na tela o tipo da variável status