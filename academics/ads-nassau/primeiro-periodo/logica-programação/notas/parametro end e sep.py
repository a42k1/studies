print('mind', end = ' ') # parametro end altera a função padrão de quebra de linha para o que você quiser
print('the gap') # então se você quiser que o print não quebre a linha, você pode usar o parametro end

print('mind', 'the', 'gap') # o separador padrão é um espaço.
print('mind', 'the', 'gap', sep = '.') # mas você pode alterar ele com o parametro sep